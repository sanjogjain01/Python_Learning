# What is List
# is one of the built-in data types in Python.
# A Python list is a sequence of comma separated items, enclosed in square brackets [ ].
# The items in a Python list need not be of the same data type.
# The major difference is that in C/C++/Java, the array elements must be of same type.
# On the other hand, Python lists may have objects of different data types.
# A Python list is mutable ie can be change
# Python support both + and _ index where + means from left to right and - means right to left
# Operation :
# 1, 2, 3] + [4, 5, 6]	[1, 2, 3, 4, 5, 6]	Concatenation
#  3 in [1, 2, 3]	True - member ship
# ['Hi!'] * 4	['Hi!', 'Hi!', 'Hi!', 'Hi!']	Repetition

